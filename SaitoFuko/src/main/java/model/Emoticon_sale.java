package model;

public class Emoticon_sale {
	private String emo_order_code;
	private String user_id;
	private Integer price;
	private String order_date;
	private String emo_id;
	public String getEmo_order_code() {
		return emo_order_code;
	}
	public void setEmo_order_code(String emo_order_code) {
		this.emo_order_code = emo_order_code;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	public String getOrder_date() {
		return order_date;
	}
	public void setOrder_date(String order_date) {
		this.order_date = order_date;
	}
	public String getEmo_id() {
		return emo_id;
	}
	public void setEmo_id(String emo_id) {
		this.emo_id = emo_id;
	}
	
}
